/**
 * Get palettes by category
 */
export function getPalettesByCategory(category: PaletteCategory): ColorPalette[] {
  return colorPalettes.filter((p) => p.category === category);
}

/**
 * Get palette by ID
 */
export function getPaletteById(id: string): ColorPalette | undefined {
  return colorPalettes.find((p) => p.id === id);
}

/**
 * Get palettes by use case
 */
export function getPalettesByUseCase(useCase: string): ColorPalette[] {
  return colorPalettes.filter((p) =>
    p.useCases.some((uc) => uc.toLowerCase().includes(useCase.toLowerCase()))
  );
}

/**
 * Get random palette
 */
export function getRandomPalette(): ColorPalette {
  return colorPalettes[Math.floor(Math.random() * colorPalettes.length)];
}

